    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
    RegistroCompra rc = new RegistroCompra();
    rc.setValorUnitario(Float.parseFloat(txtValorUnitario.getText()));
    rc.setQuantidade(Integer.parseInt(txtQuant.getText()));
    txtValorTotal.setText(String.valueOf(rc.calculo()));
        try{
            
            rc.setNomeProduto(txtProduto.getText());
            rc.setValorTotal(Float.parseFloat(txtValorTotal.getText()));
            rc.setValorUnitario(Float.parseFloat(txtValorUnitario.getText()));
            rc.setSolicitante(txtSolicitante.getText());
            rc.setAprovacao(jComboBox1.getSelectedItem().toString());
                    
            Banco dao = new Banco();
            dao.RegistrarCompra(rc);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
            
        }
        JOptionPane.showMessageDialog(null, "Registro incluido com sucesso!");
        txtProduto.setText("");
        txtSolicitante.setText("");
        txtValorTotal.setText("");
        txtValorUnitario.setText("");
        jComboBox1.setSelectedIndex(0);
        txtQuant.setText("");
    }//GEN-LAST:event_btnConfirmarActionPerformed

