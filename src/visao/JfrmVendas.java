    private void btnNovoProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoProdutoActionPerformed
        try {
            Banco banco = new Banco();
            TabelaTempo tt = new TabelaTempo();
            Temp t = new Temp();

            tt.setNome(txtProduto.getText());
            tt.setPreco(Float.parseFloat(txtPreco.getText()));
            tt.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
            
            banco.registarTempo(tt); 
            ListarTemp();
            mostraTemp(tabelatempo);
 
        } catch (Exception ex) {
            Logger.getLogger(JfrmVendas.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        total = total+((Float.parseFloat(txtPreco.getText())) * (Integer.parseInt(txtQuantidade.getText())));
        txtSubTotal.setText(String.valueOf(total));
       
       
        txtPesquisa.setText("");
        txtProduto.setText("");
        txtMarca.setText("");
        txtPreco.setText("");
        txtQuantidade.setText("");
       
    }//GEN-LAST:event_btnNovoProdutoActionPerformed

