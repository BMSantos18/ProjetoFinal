    private void mostraTemp(List<TabelaTempo> tempos) throws Exception{
        TabelaTempo tt = new TabelaTempo();
        while(tmNota.getRowCount()>0){
            tmNota.removeRow(0);
        }
        if(tempos.size()==0){
            JOptionPane.showMessageDialog(null, "Nenhuma compra foi feita");
        }else{
            String[] linha = new String[]{null, null, null};
            for(int i = 0; i < tempos.size();i++){
                tmNota.addRow(linha);
                tmNota.setValueAt(tempos.get(i).getNome(),i,0);
                tmNota.setValueAt(tempos.get(i).getPreco(),i,1);
                tmNota.setValueAt(tempos.get(i).getQuantidade(),i,2);
            }
        }
    }
    
