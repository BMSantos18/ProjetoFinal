    private void mostraPesquisa(List<Produto> estoques){
        RegistroCompra rc = new RegistroCompra(); 
        while(tmEstoque.getRowCount()>0){
            tmEstoque.removeRow(0);
        }
        if(estoques.size()==0){
            JOptionPane.showMessageDialog(null, "Nenhuma compra foi feita");
        }else{
            String[] linha = new String[]{null, null, null, null};
            for(int i = 0; i < estoques.size();i++){
                tmEstoque.addRow(linha);
                tmEstoque.setValueAt(estoques.get(i).getSolicitante(),i,0);
                tmEstoque.setValueAt(estoques.get(i).getNomeProduto(),i,1);
                tmEstoque.setValueAt(estoques.get(i).getValorTotal(),i,2);
                tmEstoque.setValueAt(estoques.get(i).getQuantidade(),i,3);
                tmEstoque.setValueAt(estoques.get(i).getMarca(),i,4);
            }
        }
    }

    
    public void ListarVendas()throws Exception{
        Banco dao = new Banco();
        estoque = dao.getEst("%" + txtProduto.getText()+ "%");
        mostraPesquisa(estoque);
    }
    
