    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        if(jComboBox1.getSelectedIndex()==2){
           try{
               int opcao = JOptionPane.showConfirmDialog(null,"Deseja Realizar alteração no registro ?","Atenção",JOptionPane.YES_NO_OPTION);
               if(opcao == JOptionPane.YES_OPTION){
                   Produto e = new Produto(); 
                   e.setId(Integer.valueOf(txtId.getText()));
                   e.setNome(txtProduto.getText());
                   e.setMarca(txtFornecedor.getText());
                   e.setQuantidade(Integer.valueOf(txtQuantidade.getText()));
                   e.setPreco(Float.valueOf(txtPreco.getText()));
                   Banco banco = new Banco();
                   banco.editarProduto(e);
                   JOptionPane.showMessageDialog(null,"Alteração realizada com sucesso.");
                }else{
                   JOptionPane.showMessageDialog(null, "Alteração não realizada.");
               }   
            }catch(Exception e){
                e.printStackTrace();
            }
        }
       
        
  
        
    }//GEN-LAST:event_btnConfirmarActionPerformed

